package javaTutorial;

public class JavaMath {

	public static void main(String[]args) {
		int x=5;
		int y=9;
		
		System.out.println(Math.max(x,y));   //find the highest value of x and y
		System.out.println(Math.min(5,6));	 //find the lowest value of x and y
		System.out.println(Math.sqrt(y));
		
	}
}
